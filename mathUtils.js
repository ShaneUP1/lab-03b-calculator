
// Addition
export function add(thing1, thing2) {
    return thing1 + thing2;
}

// Subtraction
export function subtract(thing1, thing2) {
    return thing1 - thing2;
}

// Multiplication
export function multiply(thing1, thing2) {
    return thing1 * thing2;
}

// Division
export function divide(thing1, thing2) {
    return thing1 / thing2;
}
